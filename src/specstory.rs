// ---------------------------------------------------------------------------
// Data model
// ---------------------------------------------------------------------------

#[derive(Debug, Clone, PartialEq)]
pub enum Role {
    User,
    Agent,
}

#[derive(Debug, Clone)]
pub struct ChatMessage {
    pub role: Role,
    pub timestamp: String,
    pub model: Option<String>,
    pub content: String,
}

// ---------------------------------------------------------------------------
// Detection
// ---------------------------------------------------------------------------

const SPECSTORY_HEADER: &str = "<!-- Generated by SpecStory";

pub fn is_specstory(input: &str) -> bool {
    input.trim_start().starts_with(SPECSTORY_HEADER)
}

// ---------------------------------------------------------------------------
// Parsing
// ---------------------------------------------------------------------------

/// Parse a SpecStory markdown file into a list of chat messages.
pub fn parse_specstory(input: &str) -> Vec<ChatMessage> {
    split_into_messages(input)
}

/// Split input into sections by `---` horizontal rules AND by role headers
/// (`_**User ...**_` / `_**Agent ...**_`), since multiple consecutive agent
/// messages can appear within a single `---` section.
fn split_into_messages(input: &str) -> Vec<ChatMessage> {
    let mut messages = Vec::new();
    let mut current_header: Option<(Role, String, Option<String>)> = None;
    let mut current_content = String::new();

    for line in input.lines() {
        let stripped = line.trim();

        // Skip HR separators
        if stripped == "---" || stripped == "***" || stripped == "___" {
            continue;
        }

        // Check if this line is a role header
        if let Some(parsed) = try_parse_header(stripped) {
            // Flush previous message
            if let Some((role, timestamp, model)) = current_header.take() {
                let content = current_content.trim().to_string();
                if !content.is_empty() {
                    messages.push(ChatMessage {
                        role,
                        timestamp,
                        model,
                        content,
                    });
                }
            }
            current_header = Some(parsed);
            current_content.clear();
        } else if current_header.is_some() {
            if !current_content.is_empty() {
                current_content.push('\n');
            }
            current_content.push_str(line);
        }
    }

    // Flush last message
    if let Some((role, timestamp, model)) = current_header {
        let content = current_content.trim().to_string();
        if !content.is_empty() {
            messages.push(ChatMessage {
                role,
                timestamp,
                model,
                content,
            });
        }
    }

    messages
}

fn try_parse_header(line: &str) -> Option<(Role, String, Option<String>)> {
    let inner = line.strip_prefix("_**")?.strip_suffix("**_")?;

    let (role, rest) = if let Some(rest) = inner.strip_prefix("User") {
        (Role::User, rest.trim())
    } else if let Some(rest) = inner.strip_prefix("Agent") {
        (Role::Agent, rest.trim())
    } else {
        return None;
    };

    let (timestamp, model) = parse_role_meta(rest, &role);
    Some((role, timestamp, model))
}

fn parse_role_meta(meta: &str, role: &Role) -> (String, Option<String>) {
    // meta is like "(2025-12-03T08:19:20.239Z)" or "(model-name 2025-12-03T08:20:15.123Z)"
    let inner = meta
        .strip_prefix('(')
        .and_then(|s| s.strip_suffix(')'))
        .unwrap_or(meta)
        .trim();

    match role {
        Role::User => (inner.to_string(), None),
        Role::Agent => {
            // For agent, the model name comes before the timestamp
            // Timestamp looks like an ISO date: YYYY-MM-DD...
            if let Some(ts_start) = find_timestamp_start(inner) {
                let model = inner[..ts_start].trim().to_string();
                let timestamp = inner[ts_start..].trim().to_string();
                let model = if model.is_empty() {
                    None
                } else {
                    Some(model)
                };
                (timestamp, model)
            } else {
                (inner.to_string(), None)
            }
        }
    }
}

fn find_timestamp_start(s: &str) -> Option<usize> {
    // Look for YYYY-MM-DD pattern
    for (i, _) in s.char_indices() {
        if s.len() >= i + 10 {
            let candidate = &s[i..i + 10];
            if candidate.len() == 10
                && candidate.as_bytes()[4] == b'-'
                && candidate.as_bytes()[7] == b'-'
                && candidate[..4].chars().all(|c| c.is_ascii_digit())
                && candidate[5..7].chars().all(|c| c.is_ascii_digit())
                && candidate[8..10].chars().all(|c| c.is_ascii_digit())
            {
                return Some(i);
            }
        }
    }
    None
}

