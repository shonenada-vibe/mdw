#[cfg(test)]
mod tests {
    use super::*;

    const SAMPLE: &str = r#"<!-- Generated by SpecStory, Markdown v2.0.0 -->
# 2025-12-03 08:19:20.239Z

_**User (2025-12-03T08:19:20.239Z)**_
Hello, can you help me?

---

_**Agent (claude-3-opus 2025-12-03T08:20:15.123Z)**_
Of course! How can I assist you today?

---

_**User (2025-12-03T08:21:00.000Z)**_
Fix the bug in main.rs

---

_**Agent (claude-3-opus 2025-12-03T08:22:00.000Z)**_
<think>
Let me analyze the code...
</think>

I found the issue. Here's the fix:

```rust
fn main() {
    println!("fixed");
}
```
"#;

    #[test]
    fn test_is_specstory() {
        assert!(is_specstory(SAMPLE));
        assert!(!is_specstory("# Regular markdown"));
        assert!(!is_specstory(""));
    }

    #[test]
    fn test_parse_specstory_message_count() {
        let messages = parse_specstory(SAMPLE);
        assert_eq!(messages.len(), 4);
    }

    #[test]
    fn test_parse_specstory_roles() {
        let messages = parse_specstory(SAMPLE);
        assert_eq!(messages[0].role, Role::User);
        assert_eq!(messages[1].role, Role::Agent);
        assert_eq!(messages[2].role, Role::User);
        assert_eq!(messages[3].role, Role::Agent);
    }

    #[test]
    fn test_parse_specstory_model() {
        let messages = parse_specstory(SAMPLE);
        assert_eq!(messages[0].model, None);
        assert_eq!(messages[1].model, Some("claude-3-opus".to_string()));
    }

    #[test]
    fn test_parse_specstory_timestamp() {
        let messages = parse_specstory(SAMPLE);
        assert_eq!(messages[0].timestamp, "2025-12-03T08:19:20.239Z");
    }

    #[test]
    fn test_parse_specstory_content() {
        let messages = parse_specstory(SAMPLE);
        assert_eq!(messages[0].content, "Hello, can you help me?");
        assert!(messages[1]
            .content
            .contains("Of course! How can I assist you today?"));
    }

    #[test]
    fn test_collapse_think_blocks() {
        let input = "Before\n<think>\nsome reasoning\n</think>\nAfter";
        let result = collapse_special_blocks(input);
        assert!(result.contains("[thinking...]"));
        assert!(!result.contains("some reasoning"));
        assert!(result.contains("After"));
    }

    #[test]
    fn test_collapse_tool_use_blocks() {
        let input = "Before\n<execute_command>\nsome command\n</execute_command>\nAfter";
        let result = collapse_special_blocks(input);
        assert!(result.contains("[tool: execute_command]"));
        assert!(!result.contains("some command"));
        assert!(result.contains("After"));
    }

    #[test]
    fn test_collapse_tool_use_with_data_attr() {
        let input = "Before\n<tool-use data-tool-type=\"unknown\" data-tool-name=\"Agent\"><details>\n<summary>Tool use</summary>\nstuff\n</details>\nAfter";
        let result = collapse_special_blocks(input);
        assert!(result.contains("[tool: Agent]"));
        assert!(!result.contains("stuff"));
        assert!(result.contains("After"));
    }

    #[test]
    fn test_shorten_timestamp() {
        assert_eq!(
            shorten_timestamp("2025-12-03T08:19:20.239Z"),
            "12-03 08:19"
        );
    }

    #[test]
    fn test_wrap_text() {
        let wrapped = wrap_text("hello world this is a test", 10);
        assert!(wrapped.iter().all(|l| l.len() <= 10));
        assert!(wrapped.len() >= 3);
    }
}
